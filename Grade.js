// Assignment: Find grades by marks/numbers
var Marks = 89;
if (Marks >= 90 && Marks <= 100) {
    console.log("Grade A1");
}
else if (Marks >= 80 && Marks < 90) {
    console.log("Grade A");
}
else if (Marks >= 60 && Marks < 80) {
    console.log("Grade B");
}
else if (Marks >= 50 && Marks < 60) {
    console.log("Grade C");
}
else if (Marks >= 40 && Marks < 50) {
    console.log("Grade D");
}
else {
    console.log("Fail");
}
